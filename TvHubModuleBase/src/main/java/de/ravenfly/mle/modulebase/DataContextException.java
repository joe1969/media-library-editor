package de.ravenfly.mle.modulebase;

public class DataContextException extends Exception {

	private static final long serialVersionUID = -8285444476235444160L;

	public DataContextException() {
		super();
	}

	public DataContextException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataContextException(String message) {
		super(message);
	}

	public DataContextException(Throwable cause) {
		super(cause);
	}
}
